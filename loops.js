// Example 1: Print iterations from 1 to 5 using for loop

for (let i = 1; i <= 5; i++) {
  console.log("Iteration number : "+ i);
}

// Example 2: Calculate the sum of numbers from 1 to 10

let sum = 0;

for (let i = 1; i <= 10; i++) {
  sum += i;
}

console.log("Sum : "+ sum);

// Example 3: Print iterations from 1 to 5 using while loop

let count = 1;

while (count <= 5) {
  console.log("Iteration number : "+ count);
  count++;
}
